        #region 创建目录
        /// <summary>
        /// 创建目录
        /// </summary>
        /// <param name="dir">要创建的目录路径包括目录名</param>
        public static void CreateDir(string dir)
        {
            if (dir.Length == 0) return;
            if (!Directory.Exists(System.AppDomain.CurrentDomain.BaseDirectory + "\\" + dir))
                Directory.CreateDirectory(System.AppDomain.CurrentDomain.BaseDirectory + "\\" + dir);
        }
        #endregion

        #region 删除目录
        /// <summary>
        /// 删除目录
